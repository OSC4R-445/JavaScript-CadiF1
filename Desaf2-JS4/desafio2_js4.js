// Obtener todos los elementos necesarios del DOM
const $ = id => document.getElementById(id); // Función auxiliar para acortar document.getElementById

// Botones de la navegación
const loginBtn = $('loginButton'), registerBtn = $('registerButton'), logoutBtn = $('logoutButton');
// Modales de inicio de sesión y registro
const loginModal = $('loginModal'), registerModal = $('registerModal');
// Formularios dentro de los modales
const loginForm = $('loginForm'), registerForm = $('registerForm');
// Elementos para mostrar mensajes de error/éxito
const loginMsg = $('loginMessage'), registerMsg = $('registerMessage');

/**
 * @function updateUI
 * @description Actualiza la visibilidad de los botones del header
 * (Iniciar Sesión, Registro, Cerrar Sesión) basado en si hay un usuario logueado en localStorage.
 * Cumple con el requisito 4.
 */
const updateUI = () => {
    const user = localStorage.getItem('usuarioLogueado'); // Busca si hay un usuario logueado
    if (user) {
        // Si hay un usuario, oculta los botones de login/registro y muestra el de cerrar sesión
        loginBtn.classList.add('hidden');
        registerBtn.classList.add('hidden');
        logoutBtn.classList.remove('hidden');
    } else {
        // Si no hay usuario, muestra los botones de login/registro y oculta el de cerrar sesión
        loginBtn.classList.remove('hidden');
        registerBtn.classList.remove('hidden');
        logoutBtn.classList.add('hidden');
    }
};

// Open and close modals
const openModal = modal => { modal.style.display = 'block'; };
const closeModal = modal => { modal.style.display = 'none'; };

// Manejo del envío del formulario de inicio de sesión (Requisito 2 y 3)
loginForm.addEventListener('submit', e => {
    e.preventDefault(); // Evita el envío por defecto del formulario que recargaría la página
    const username = $('loginUsername').value;
    const password = $('loginPassword').value;

    // Busca el usuario por su nombre de usuario en el arreglo global 'usuarios'
    const userFound = usuarios.find(u => u.username === username);

    if (userFound && userFound.password === password) {
        // Usuario y contraseña válidos: guarda el usuario en localStorage
        localStorage.setItem('usuarioLogueado', JSON.stringify(userFound));
        loginMsg.textContent = 'Login successful!';
        loginMsg.style.color = 'green';
        // Cierra el modal y actualiza la UI después de un breve retraso
        setTimeout(() => { closeModal(loginModal); updateUI(); }, 500);
    } else {
        // Muestra mensaje de error si el usuario no existe o la contraseña es incorrecta
        loginMsg.textContent = 'Invalid username or password.';
        loginMsg.style.color = 'red';
    }
});

// Handle registration
registerForm.addEventListener('submit', e => {
    e.preventDefault(); // Evita el envío por defecto del formulario
    const name = $('registerName').value;
    const username = $('registerUsername').value;
    const email = $('registerEmail').value;
    const password = $('registerPassword').value;

    // Verifica si el nombre de usuario o email ya existen (Requisito 3)
    if (usuarios.some(u => u.username === username || u.email === email)) {
        registerMsg.textContent = 'Username or email already exists.';
        registerMsg.style.color = 'red';
        return;
    }

    const newId = usuarios.length ? Math.max(...usuarios.map(u => u.id)) + 1 : 1;
    // Crea el nuevo objeto usuario (Requisito 2)
    const newUser = { id: newId, password, name, username, email };

    usuarios.push(newUser);
    // Guarda el nuevo usuario como logueado y para la persistencia de sesión (Requisito 5)
    localStorage.setItem('usuarioLogueado', JSON.stringify(newUser));
    localStorage.setItem('registeredUserOnSession', JSON.stringify(newUser)); // For premise 5

    registerMsg.textContent = 'Registration successful!';
    registerMsg.style.color = 'green';
    // Cierra el modal y actualiza la UI
    setTimeout(() => { closeModal(registerModal); updateUI(); }, 500);
});

// Handle logout
logoutBtn.addEventListener('click', () => {
    localStorage.removeItem('usuarioLogueado');
    localStorage.removeItem('registeredUserOnSession'); // Clear on logout
    updateUI();
    alert('Logged out.');
});

// Modal button and outside click handlers
loginBtn.addEventListener('click', () => { openModal(loginModal); loginForm.reset(); loginMsg.textContent = ''; });
registerBtn.addEventListener('click', () => { openModal(registerModal); registerForm.reset(); registerMsg.textContent = ''; });
document.querySelectorAll('.close-button').forEach(btn => {
    btn.addEventListener('click', e => closeModal(e.target.closest('.modal')));
});
window.addEventListener('click', e => {
    if (e.target === loginModal) closeModal(loginModal);
    if (e.target === registerModal) closeModal(registerModal);
});

// Lógica que se ejecuta cuando el DOM está completamente cargado (Requisito 5)
document.addEventListener('DOMContentLoaded', () => {
    // Intenta recuperar un usuario que se haya registrado en la sesión anterior
    const registeredUserInLS = localStorage.getItem('registeredUserOnSession');
    if (registeredUserInLS) {
        try {
            const user = JSON.parse(registeredUserInLS);
            // Si el usuario no está ya en el arreglo 'usuarios' (para evitar duplicados), lo añade
            if (!usuarios.some(u => u.id === user.id)) {
                usuarios.push(user);
            }
        } catch (e) {
            console.error("Error parsing registered user from localStorage:", e);
            localStorage.removeItem('registeredUserOnSession'); // Limpia datos corruptos
        }
    }
    updateUI(); // Asegura que los botones se muestren correctamente al cargar la página
});